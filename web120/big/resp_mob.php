<article class="research main">

    <h3>Either/Or/And?</h3>

    <p>In today’s business landscape having a web presence is arguably a vital and necessary part of being competitive and sustainable. It is not a question of having a website (responsive or mobile) or mobile application, it is which platforms make the most sense for a businesses needs. As a web designer/developer it is imperative to understand the benefits of available options and advise clients accordingly.</p>

    <p>There are many comparisons to which is better, lists of pros and cons, and opinions of why to utilize one technology or the other. In my thinking, it is not about better or worse, it is how to best meet the goals of a business. Web professionals need to recognize all the technologies available and how each may contribute to the success of a company. </p>

    <h3>Responsive Site vs? Mobile App</h3>

    <p>Responsive websites use HTML and CSS to create a site that responds to the device on which it is being viewed. By using what is called media queries, a site’s CSS can shape the layout, size, and displayable elements to make pages that are functional and elegant on many devices. </p>

    <p>Mobile apps are software that are designed for specific devices and operating systems, particularly tablets and smartphones. These fall into several categories: Native, Web-based, and Hybrid. Native apps runs on the platforms for which they are designed. Web-based include internet connectivity for some or all of its functionality. And Hybrids amalgamate aspects of both native and hybrid.</p>

    <h3>Apples or Oranges… or Bananas?</h3>

    <p>When choosing which to embrace there are many options to consider. Among the deciding factors (see table below)… cost, maintenance, marketing, etc… the leading component is user experience (UX). Which technologies to utilize depend on what their customers prefer, desire, and expect. With an estimated 3.5 billion smartphones users (estimated 45% of the world’s population), the UX seems like a foundational element. A business needs to assess their customer base, then implement web strategies that work for those customers. This could result in utilizing multiple technologies.</p>

    <h3>Yes, And...</h3>

    <p>The minimum, in my opinion, every business should have a functional and responsive website. From the smallest company to the largest enterprise, it is the merest essential of online presence. Case and point, mobile app developers have websites. And mobile app may be the right choice to ensure continued success of a business. Websites and mobile apps are not at odds, but instead are complementary technologies that serve similar purposes for a business… support success, optimize awareness, and attract and maintain customers.</p>


