    <h3>Power of Images</h3>
    <p>From the first pictures etched into stone to artwork of the ages to modern photography, pictures have influenced humans as long as humans have been creating them. MIT studies state the human brain can identify images in the blink of an eye (roughly 13 milliseconds). So it is no surprise that in the expanding universe of the internet that images, both photographs and illustrations, play a significant role in web design. The ever important user experience (UX) can be greatly affected by the images utilized in a website. </p>
    <h3>A 1000 Words</h3>
    <p>If a picture is worth a 1000 words, how many words are in a gallery? The answer through the UX lense is that it depends on the picture and the context in which it is used. Galleries done well highlight products, depict stories, and relay information in ways that can invoke emotion and connection. They may also be a source of irritation, confusion, and ultimately turn away potential clients if utilized poorly.</p>

    <p>A properly thought out gallery can do several things for a business: visual representation of products and services, promote a brand, create lead generation, and boost searchability with Google or Bing. In the growing online shopping industry, images replace the touch factor. A well designed gallery can decide if a customer will browse through merchandise, move on to another site, or worse abandon their cart midstream. A thousand beautiful photos are meaningless if no one wants to look at them because of flawed presentation.</p>
    <h3>UX in Mind</h3>
    <p>A successful gallery meets the needs of the client and MUST, in my opinion, be created with UX in mind. Here’s a short list of best practices to guide how to keep clients gazing:</p>
    <ul>
        <li>Use relevant images. Makes no sense to have a beautiful flower in a gallery sharing the latest flavors of ice cream.</li>
        <li>Select high-quality assets. Think about the devices and platforms the gallery will be viewed on, and design accordingly. Make it look good on any device.</li>
        <li>Write clear and relevant captions. Describe what isn’t seen in the image in one to three sentences, avoid the obvious.</li>
        <li>Assure the navigation is easy and intuitive, utilize arrows and hint at the next image.</li>
        <li>Update galleries often, keeping it up to date inspires more visits.</li>
        <li>With accessibility at the very least use ‘alt’ text, and consider ARIA tags.</li>
        <li>Be mindful of mobile users, hint for next image, utilize swipe navigation, and have a full screen option.</li>

    </ul>

    <p>Galleries are powerful tools to engage users. Use them wisely.</p>

