    <h3>Shopping the Cart</h3>

    <p>When choosing a shopping cart, there are two basic categories: web-based and self-hosted. Which to opt for depends on the needs of one’s business. The first step in the process is to make note of all the needs of the business. What products are to be sold, and are there variations in those products, such sizes or colors options? What kind of payment options are desired? Is a merchant account for processing credit cards required? Does the cart need to be able to handle shipping, tax calculation, etc.? The clearer the associated needs are, the easier it will be to decide what direction to go. And the ultimate question, what kind of user experience is desired… Who will be using the carts, and what are their needs?</p>

    <h3>Web or Self?</h3>

    <p>Once a comprehensive list has been compiled, next choosing to utilize web-based services or to self-host becomes a bit easier. What kind of shopping experience is desired? How much control of the look and options is optimal? What is the budget for the cart? How much maintenance is to be handled in house? How much software support is necessary? What kind of security is required? All of these questions and more should be taken into account before making a decision. </p>

    <h3>Pros and Cons</h3>

    <p>Each option has pros and cons. Web hosted carts can be pricey, and may have limited ability to customize the cart, and upgrading platforms can be challenging and expensive. On the flip side, hosted sites are often all inclusive for ecommerce, and they can be much easier to manage, and there are often support teams to assist.</p>

    <p>Self-hosted carts have a range of pricing, and are far more customizable to fit the desired shopping experience. A merchant account may also be a necessity, and security lies in the hands of the business. Maintenance and managing the cart is a major consideration, also falls to the business to handle. </p>

    <p>In the end, it is truly about the goals of the business and what kind experience is desired for the customer using the site. It may be best to start with a hosted site, and then as the business grows shift to self-hosted to better suit the needs of the business. </p>

